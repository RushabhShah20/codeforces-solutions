void solve()
{
    string s;
    cin >> s;
    unordered_set<char> t;
    for (const char c : s)
    {
        t.insert(c);
    }
    if (t.size() % 2 == 0)
    {
        cout << "CHAT WITH HER!" << endl;
    }
    else
    {
        cout << "IGNORE HIM!" << endl;
    }
}

